#include<bits/stdc++.h>

using namespace std;

int main(){

    int num1 = 10, num2 = 20;

    cout<<(num1<num2)<<endl;  //1
    cout<<(num1>num2)<<endl;  //0
    cout<<(num1<=num2)<<endl;  //1
    cout<<(num1>=num2)<<endl;  //0
    cout<<(num1==num2)<<endl;  //0
    cout<<(num1!=num2)<<endl;  //1

    return 0;
}